                    <h1>数独</h1>
                    <p>各パズルはただ一つの正解があります。当てることはなくて、ロジック的な判断で正解を得られます。ブランクに１から9までの数字を入れます。各行、列とブロックに1から9まで含まらなければならないことに注意してください。がんばってね!</p>
                    <p><a href="/" class="black">数独</a>（すうどく）とは、ニコリのパズル雑誌「パズル通信ニコリ」で使用される名称で、「数字は独身に限る」の略である。「数独（スウドク）」はニコリの登録商標(第3327502号など)であり、日本では「ナンバープレース（ナンプレ）」と呼ばれることも多い。日本以外の国ではSudokuという呼称が一般的だが、アメリカでは<a href="/" class="black">Sudoku</a>、Number Place両方とも使われている。</p>


                    <h2>数独のルール</h2>
                    <p>数独は、９×９のマスの中に、１から９までの数字を入れていくパズルである。</p>
                    <p>次のような三つのルールは守らなければならない。</p>
                    <ul>
                        <li>一列で同じ数字を使ってはいけない </li>
                        <li>一行で同じ数字を使ってはいけない </li>
                        <li>一つのブロックで同じ数字を使ってはいけない </li>
                    </ul>
                    <p>ルールとしては、たったこれだけだ。驚くほどシンプルだ。</p>

